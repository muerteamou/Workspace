package ejemplos;



public class Ej05Operadores2 {
  public static void main(String[] args) {
    int x;
    x = 500;
    // Valdría int x=500
    System.out.println("x vale: " + x);
    System.out.println("x + 10 vale: " + (x + 10));
    System.out.println("x - 10 vale: " + (x-10) );
    System.out.println("x * 10 vale: " + (x * 10));
    System.out.println("x / 10 vale: " +(x / 10));
    System.out.println("El resto de x entre 10 vale: " + (x % 10));
   }
}
